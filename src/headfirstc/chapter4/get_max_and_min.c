/*
This is a program to get maximums and minimums in c
*/

#include<stdio.h>
#include<limits.h>
#include<float.h>


int main(int argc, char const *argv[])
{
    printf("The maximum integer is %i \n", INT_MAX);
    printf("The minimum integer is %i \n", INT_MIN);


    printf("The maximum floating value is %f \n", FLT_MAX);
    printf("The minimum floating value is %f \n", FLT_MIN);
    return 0;
}
